package com.testifytas;

public class Task2 {
    public static void main(String[] args) {


        //Task 2: Print out all 6 variables that are all different data types on your console

        //1: String data type
        String name = "Ebere";
        System.out.println(name);

        //2: Integer data type
        int num = 12;
        System.out.println(num);

        //3: Boolean data type
        boolean available = true;
        System.out.println(available);

        //4: Float data type
        float deciNum = 1234567.23F;
        System.out.println(deciNum);

        //5: Char data type
        char oneLetterWord = 'A';
        System.out.println(oneLetterWord);

        //6: Long data type
        long length = 234567890123456787L;
        System.out.println(length);
    }
}
